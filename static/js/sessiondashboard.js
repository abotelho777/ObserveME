function showKappa() {
    document.getElementById("kappaBtn_s").style.display = "none";
    document.getElementById("kappaBtn_h").style.display = "block";
    document.getElementById("kappaDiv").style.display = "block";
}

function hideKappa() {
    document.getElementById("kappaBtn_s").style.display = "block";
    document.getElementById("kappaBtn_h").style.display = "none";
    document.getElementById("kappaDiv").style.display = "none";
}

